public class foreachloop1 {
    public static void main(String args[]) {
        String array1[] = { "1", "2", "3" };
        int[] array12 = { 1, 2, 3 };
        int i;
        for (i = 0; i < array1.length; i++) {
            System.out.print(array1[i] + " ");
            System.out.println(array12[i]);

        }
    }
}
