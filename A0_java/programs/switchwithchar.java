public class switchwithchar {
    public static void main(String[] args) {
        char num = 'a';
        switch (num) {
            case 'a':
                System.out.println("number is 0");
                break;
            case 'b':
                System.out.println("number is 1");
                break;
            default:
                System.out.println(num);
        }
    }
}
