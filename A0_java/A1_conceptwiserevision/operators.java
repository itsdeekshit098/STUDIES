package conceptwiserevision;

public class operators 
{
public static void main(String[] args) 
{
    int a=5;
    int b=4;
    //bitwise operations;
    System.out.println("value of a&b is"+(a&b));
    System.out.println("value of a&b is "+(a|b));
    System.out.println("value of a&b is "+(a^b));
    System.out.println("value of a&b is "+(~a));
    System.out.println(a>>1);
    System.out.println(a<<1);
}   
}
