public class switch1 {
    public static void main(String[] args) {
        char a='a';
        switch(a)
        {
            case 'a':
            {
                System.out.println("case 1");
            }
            case 'b':
            {
                System.out.println("case 2");
            }
            case 'c':
            {
                System.out.println("case 3");
            }
        }
    }
}
