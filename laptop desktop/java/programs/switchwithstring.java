public class switchwithstring {

    public static void main(String[] args) {
        String num = "hansi";
        switch (num) {
            case "hansi":
                System.out.println("number is 0");
                break;
            case "chinnu":
                System.out.println("number is 1");
                break;
            default:
                System.out.println(num);
        }
    }

}
