public class switch3 {
    public static void main(String[] args) {
        String a="hansi1";
        switch(a)
        {
            case "hansi":
            {
                System.out.println("case 1");
                break;
            }
            case "venu":
            {
                System.out.println("case 2");
                break;
            }
            case "chinni":
            {
                System.out.println("case 3");
            }
            default:
            {
                System.out.println("from default");
            }
        }
    }
}
